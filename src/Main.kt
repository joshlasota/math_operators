fun main() {
    val a = 10
    val b = 5

    val resultAdd = a + b
    println("$a + $b = $resultAdd")

    val resultSub = a - b
    println("$a - $b = $resultSub")

    val resultMul = a * b
    println("$a * $b = $resultMul")

    val resultDiv = a / b
    println("$a / $b = $resultDiv")

    val resultMod = a % b
    println("$a % $b = $resultMod")
}